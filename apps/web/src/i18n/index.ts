export type TranslationKey = keyof (typeof dict)['vi'];

export function useT(): (key: TranslationKey, params?: Record<string, string>) => string {
  const lang = useSettingsStore((s) => s.language);
  return (key, params) => {
    const raw = dict[lang][key] ?? dict.vi[key] ?? key;
    if (!params) return raw;
    return raw.replace(/\{(\w+)\}/g, (_, p: string) => params[p] ?? '');
  };
}

/** Non-hook version cho code không nằm trong React (toast, etc). */
export function t(lang: 'vi' | 'en', key: TranslationKey, params?: Record<string, string>): string {
  const raw = dict[lang][key] ?? dict.vi[key] ?? key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, p: string) => params[p] ?? '');
}
